<h2>The Concept:</h2>

<p><em>"Place That Price"</em> is a captivating game that leverages the vast database of homes discovered by users on our platform. In this game, you'll be presented with a property that another user has found, and your mission is simple: guess the price within 20 seconds! The closer your estimate is to the actual price, the more points you'll earn, with a maximum of 10,000 points per property. Can you accurately gauge the value of homes from different states, property types, and sizes?</p>

<h2>How It Works:</h2>

<ol>
	<li><strong>Property Details:</strong> Each round of <em>"Place That Price"</em> provides you with essential property details, including the state in which the property is located, the type of property (e.g., house, apartment, condo), the number of bedrooms and bathrooms, living size, lot size, and an image of the property.</li>

	<li><strong>Guess the Price:</strong> Armed with this information, you'll have 20 seconds to make your best estimate of the property's price. Will you rely on your intuition, knowledge of the real estate market, or a combination of both?</li>

	<li><strong>Scoring:</strong> Your score for each property is based on how close your guess is to the actual price. The closer you are, the higher your score, with a maximum of 10,000 points available for each property.</li>

	<li><strong>Five Rounds:</strong> The game consists of five exciting rounds, each featuring a different property. Can you maintain your accuracy throughout the game?</li>
</ol>

